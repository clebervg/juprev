def _calcular_acumulados(dados: list[tuple[int, int, str]]) -> list[dict]:
    """Calcula o índice acumulado em cascata. Base: 07/1994 = 1.0"""
    acumulado = Decimal("1.0000000000")
    registros = []

    for ano, mes, variacao_str in dados:
        variacao = Decimal(variacao_str) / Decimal("100")
        acumulado = (acumulado * (Decimal("1") + variacao)).quantize(
            Decimal("0.0000000001"), rounding=ROUND_HALF_UP
        )
        registros.append({
            "competencia": date(ano, mes, 1),
            "fonte": "INPC",
            "indice_mensal": variacao.quantize(Decimal("0.00000001"), rounding=ROUND_HALF_UP),
            "indice_acumulado": acumulado,
        })

    return registros


async def _seed(url: str) -> None:
    engine = create_async_engine(url, echo=False)
    async_session = sessionmaker(engine, class_=AsyncSession, expire_on_commit=False)

    registros = _calcular_acumulados(INPC_HISTORICO)

    async with async_session() as session:
        # Apaga apenas registros INPC para permitir re-execução segura
        await session.execute(text("DELETE FROM indices_correcao WHERE fonte = 'INPC'"))

        for r in registros:
            session.add(IndiceCorrecao(**r))

        await session.commit()

    await engine.dispose()
    print(f"✓ {len(registros)} registros INPC inseridos (07/1994 → {registros[-1]['competencia']})")
    print(f"  Acumulado total: {registros[-1]['indice_acumulado']}x")


if __name__ == "__main__":
    from app.core.config import settings as cfg
