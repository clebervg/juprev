def _gerar_registros() -> list[dict]:
    """
    Gera a lista de dicionários para inserção na tabela indices_correcao.
    Calcula indice_acumulado em cadeia a partir da base 07/1994 = 1.0.
    """
    agora = datetime.now(timezone.utc).replace(tzinfo=None)
    rows = []

    # Mês base: julho/1994 — indice_mensal = 0, indice_acumulado = 1.0
    acumulado = Decimal("1.0")
    rows.append(
        {
            "id": str(uuid.uuid4()),
            "competencia": date(1994, 7, 1),
            "fonte": "INPC",
            "indice_mensal": Decimal("0"),
            "indice_acumulado": acumulado,
            "created_at": agora,
            "updated_at": agora,
        }
    )

    # Percorre em ordem cronológica
    for (ano, mes), taxa_pct in sorted(INPC_MENSAL_PCT.items()):
        mensal = Decimal(str(taxa_pct)) / Decimal("100")
        acumulado = (acumulado * (Decimal("1") + mensal)).quantize(
            Decimal("0.0000000001"), rounding=ROUND_HALF_UP
        )
        rows.append(
            {
                "id": str(uuid.uuid4()),
                "competencia": date(ano, mes, 1),
                "fonte": "INPC",
                "indice_mensal": mensal.quantize(Decimal("0.00000000"), rounding=ROUND_HALF_UP),
                "indice_acumulado": acumulado,
                "created_at": agora,
                "updated_at": agora,
            }
        )

    return rows


